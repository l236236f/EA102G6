package com.notice.model;

public class NoticeVO {
	private String notNo;
	private String memNo;
	private String notClass;	
	private String notCont;
	
	public String getNotNo() {
		return notNo;
	}
	public void setNotNo(String notNo) {
		this.notNo = notNo;
	}
	public String getMemNo() {
		return memNo;
	}
	public void setMemNo(String memNo) {
		this.memNo = memNo;
	}
	public String getNotClass() {
		return notClass;
	}
	public void setNotClass(String notClass) {
		this.notClass = notClass;
	}
	public String getNotCont() {
		return notCont;
	}
	public void setNotCont(String notCont) {
		this.notCont = notCont;
	}
	
	
}
